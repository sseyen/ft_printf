#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	va_list	ap;
	int		count;

	va_start(ap, format);
	count = 0;
	while (*format)
	{
		if (*format == '%')
			count += ft_printelem(++format, &ap);
		else
			count += ft_printchar(*format);
		format++;
	}
	va_end(ap);
	return (count);
}
